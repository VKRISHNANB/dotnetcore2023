using System;
namespace CalculatorLibrary
{
    public class Calculator
    {
        public static int Add(int v1,int v2)
        {
            return v1+v2;
        }
        public static int Divide(int v1,int v2)
        {
            return v1/v2;
        }
        public static int Multiply(int v1,int v2)
        {
            return v1*v2;
        }

    }
}
